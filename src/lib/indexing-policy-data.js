const gscObservedGlossarySet = new Set(gscObservedGlossarySlugs);
const strategicGlossarySet = new Set(strategicGlossarySlugs);
const calculatorSupportedGlossarySet = new Set(calculatorSupportedGlossarySlugs);

export function getSharedGlossaryIndexingReason({
  slug,
  hasNextStep = false,
}) {
  if (gscObservedGlossarySet.has(slug)) {
    return "gsc-observed";
  }

  if (hasNextStep) {
    return "glossary-next-step";
  }

  if (calculatorSupportedGlossarySet.has(slug)) {
    return "calculator-supported";
  }

  if (strategicGlossarySet.has(slug)) {
    return "strategic-glossary";
  }

  return "low-value-glossary-first-pass";
}

export function isSharedGlossaryIndexable(input) {
  return getSharedGlossaryIndexingReason(input) !== "low-value-glossary-first-pass";
}
